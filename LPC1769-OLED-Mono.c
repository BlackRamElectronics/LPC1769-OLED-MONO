//====================================================================================
void SPI_Send_Bitbash(unsigned char byte)
{
	uint32_t delay, bit_count;
	unsigned char temp_byte = byte;
	
	GPIO0->FIOCLR = 1 << 16;	// CS low
	
	for(bit_count = 0; bit_count < 8; bit_count++)
	{
		GPIO0->FIOCLR = 1 << 15;	// SCK low
		if(temp_byte & 0x80)
		{
			GPIO0->FIOSET = 1 << 18;	// SCK high
		}
		else
		{
			GPIO0->FIOCLR = 1 << 18;	// SCK low
		}
		temp_byte= temp_byte << 1;
		
		for(delay = 0; delay < 10; delay++);
		
		GPIO0->FIOSET = 1 << 15;	// SCK high
		for(delay = 0; delay < 10; delay++);
	}
	
	GPIO0->FIOSET = 1 << 16;	// CS high
	for(delay = 0; delay < 10; delay++);
}

//====================================================================================
void SendCMD(unsigned char cmd)
{
	GPIO0->FIOCLR = 1 << 21;	// CD high
	SPI_Send_Bitbash(cmd);
}

//====================================================================================
void SendData(unsigned char data)
{
	GPIO0->FIOSET = 1 << 21;	// CD low
	SPI_Send_Bitbash(data);
}

//====================================================================================
void SPI_Init()
{
	uint32_t tmp, prescale, cr0_div, cmp_clk, ssp_clk, target_clock;
	
	target_clock = 1000000;
	
	// Enable pins for SPI
	PINCON->PINSEL0 |= (1<<31);
	PINCON->PINSEL1 |= (1<<1);
	PINCON->PINSEL1 |= (1<<5);
	

	// Set up clock and power for SPI module
	SC->PCONP |= (1<<21);

	// Configure SPI, interrupt is disable as default
	SSP0->CR0 = 0x000E;

	SSP0->CR1 = 0;

	
	// Configure clock
    //ssp_clk = CLKPWR_GetPCLK(CLKPWR_PCLKSEL_SSP0);
	ssp_clk = (SC->PCLKSEL1 & ((1<<10) | (1<<11))) >> 10;
	
	/* Find closest divider to get at or under the target frequency.
	   Use smallest prescale possible and rely on the divider to get
	   the closest target frequency */
	cr0_div = 0;
	cmp_clk = 0xFFFFFFFF;
	prescale = 2;
	while (cmp_clk > target_clock)
	{
		cmp_clk = ssp_clk / ((cr0_div + 1) * prescale);
		if (cmp_clk > target_clock)
		{
			cr0_div++;
			if (cr0_div > 0xFF)
			{
				cr0_div = 0;
				prescale += 2;
			}
		}
	}

    /* Write computed prescaler and divider back to register */
    SSP0->CR0 |= (cr0_div << 8);
    SSP0->CPSR = prescale;
}

